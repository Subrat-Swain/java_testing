package com.anvsystems.javaOOPDemo;

public class Laptop {
	String company;
	int ram;
	String SSD;
	int screenSize;
	String processor;
	String GPU;
	
	public static void main(String[] args) {
		Laptop Asus = new Laptop();
		Asus.company = "Asus";
		Asus.ram = 8;
		Asus.SSD = "512GB";
		Asus.screenSize = 17;
		Asus.processor = "RYZEN 7";
		Asus.GPU = "GTX GEFORCE nVIDIA";
	}
}
	
