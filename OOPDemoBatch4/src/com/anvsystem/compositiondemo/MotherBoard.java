package com.anvsystem.compositiondemo;

public class MotherBoard {

	String processor;
	String ramCapacity;
	String powerRange;
	String port;
	@Override
	public String toString() {
		return "MotherBoard [processor=" + processor + ", ramCapacity=" + ramCapacity + ", powerRange=" + powerRange
				+ ", port=" + port + "]";
	}
	
}
